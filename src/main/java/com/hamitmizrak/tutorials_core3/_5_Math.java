package com.hamitmizrak.tutorials_core3;

public class _5_Math {

    static void main() {
        System.out.println("En küçük sayı: "+Math.min(10,900));
        System.out.println("En büyük sayı: "+Math.max(10,900));

        System.out.println("Karekök: "+Math.sqrt(16));
        System.out.println("Mutlak: "+Math.abs(-16));
        System.out.println("Üslü: "+Math.pow(2,5));

        System.out.println("Sinüs: "+Math.sin(45));
        System.out.println("Cosinüs: "+Math.cos(45));

        System.out.println("alta yuvarla: "+Math.floor(14.9));
        System.out.println("üste yuvarla: "+Math.ceil(14.0001));
        System.out.println("ortalama yuvarla: "+Math.round(14.1));
    }
}
